import streamlit as st

st.set_page_config(page_title="Бенчмарки", layout="wide")

st.title("🎯 Бенчмарки для LLM")

st.markdown(
    """
Приложение помогает **подготавливать, размечать, генерировать и оценивать** диалоги между пользователем и ассистентом для оценки того, как ведут себя модели, настраивать промпты под конкретные задачи, отслеживать как их изменения влияют на результат.

👉 **Рекомендуемый порядок работы:**
1. **Сначала загрузите и разметьте данные** (*Разметка*).
2. При необходимости **дополните данные** сгенерировав диалоги (*Генерация*).
3. При необходимости — **проанализируйте и отредактируйте** данные (*Анализ и редактирование данных*).
4. **Запустите бенчмарк**, чтобы оценить качество решения.
5. **Проанализируйте результаты** на соответствующей странице.

Ниже — краткое описание каждой страницы.
"""
)

st.subheader("🚀 Быстрый переход")

cols = st.columns(5)

with cols[0]:
    if st.button("📝\nРазметка", use_container_width=True):
        st.switch_page("pages/01_Разметка.py")
with cols[1]:
    if st.button("🔄\nГенерация", use_container_width=True):
        st.switch_page("pages/02_Генерация.py")
with cols[2]:
    if st.button("📊\nАнализ и редактирование", use_container_width=True):
        st.switch_page("pages/03_Анализ_и_редактирование_данных.py")
with cols[3]:
    if st.button("📊\nБенчмарк", use_container_width=True):
        st.switch_page("pages/04_Бенчмарк.py")
with cols[4]:
    if st.button("🔍\nРезультаты", use_container_width=True):
        st.switch_page("pages/07_Результаты.py")

row2 = st.columns(5)
with row2[0]:
    if st.button("🧑‍⚖️\nLLM-судья", use_container_width=True):
        st.switch_page("pages/08_Настройки_LLM_судьи.py")
with row2[1]:
    if st.button("🤖\nБенчмарк агентов", use_container_width=True):
        st.switch_page("pages/09_Бенчмарк_агентов.py")

# Подробное описание каждой страницы
st.subheader("📚 Описание страниц")

with st.expander("📝 Разметка"):
    st.write(
        """
    Возможность первичной разметки диалогов с указанием целей.  
    - Загрузка файлов с расшифровкой диалогов.  
    - Ввод списка допустимых целей.  
    - Поддержка ручной разметки или автоматической разметки с помощью LLM.  
    - Сохранение размеченных данных.
    """
    )

with st.expander("🔄 Генерация"):
    st.write(
        """
    Возможность генерации новых диалогов с помощью LLM, попхожих по стилю и структуре к загруженным примерам.  
    - Загрузка исходных диалогов.  
    - Указание желаемых целей для генерируемых диалогов.  
    - Редактирование системного промпта для генерации.  
    - Сохранение оригинальных и сгенерированных диалогов в едином файле.
    """
    )

with st.expander("📊 Анализ и редактирование данных"):
    st.write(
        """
    Просмотр, анализ, редактирование и курирование датасета диалогов.  
    - Загрузка JSONL из файла или из базы (Postgres + MinIO).  
    - Исключение диалогов из выборки, ручное добавление новых.  
    - Опциональная **кластеризация** для поиска тем.  
    - Экспорт и сохранение отфильтрованной выборки в БД.
    """
    )

with st.expander("📊 Бенчмарк"):
    st.write(
        """
    Автоматический запуск оценки диалоговой системы на основе размеченного датасета.  
    - Выбор размеченного набора диалогов.  
    - Прогон диалогов через симулятор пользователя с учётом заданных целей.  
    - Поддержка различных критериев оценки.  
    - Фиксация результатов и ошибок для последующего анализа.
    """
    )

with st.expander("🔍 Результаты"):
    st.write(
        """
    Возможность просмотра и анализа результатов бенчмарка в удобной форме.  
    - Отображение успешных и неудачных диалогов.
    - Просмотр логов и деталей ошибок.  
    - Экспорт метрик и сырых результатов.
    """
    )

with st.expander("🧑‍⚖️ Калибровка LLM-судьи"):
    st.write(
        """
    Калибровка LLM-оценщика по размеченному датасету.  
    - Загрузка JSONL и выбор случайной выборки для разметки.  
    - Ручная разметка, прогон судьи, сравнение с человеком (κ, α, корреляции, exact match).  
    - Сравнение моделей, настройка промпта, пресеты.
    """
    )

with st.expander("🤖 Бенчмарк агентов"):
    st.write(
        """
    Оценка агентов по трассировкам **Langfuse**.  
    - Фильтры выгрузки (период, имя trace, metadata, thread_id).  
    - Просмотр истории диалогов из trace.  
    - Прогон **LLM-судьи** и экспорт результатов.
    """
    )

st.info("💡 Вы также можете переключаться между страницами через меню слева.")
